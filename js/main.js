let a = 6;
let b = 3;

let sum = a + b;
let sub = a - b;
let mul = a * b;
let div = a / b;
let exp = a ** b;

console.log('6 + 3 =', sum, '--//----(сложение)');
console.log('6 - 3 =', sub, '--//----(вычитание)');
console.log('6 * 3 =', mul, '--//---(умножение)');
console.log('6 / 3 =', div, '--//----(деление)');
console.log('6 ** 3 =', exp, '--//-(возведение в степень)');